    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked

        Alunos aluno = new Alunos();
        if ("".equals(jTxtNoMatricula.getText())) {
            JOptionPane.showMessageDialog(null, "Número de matrícula não informado.");
            enab.setEnabled(false);
            jTxtNoMatricula.setText("");
            jTxtNoMatricula.requestFocus();
        } else {
            try {
                String nomatricula = jTxtNoMatricula.getText();
                aluno = alunosDAO.getPesquisaPorMat(nomatricula);

                if (aluno != null) {
                    //JOptionPane.showMessageDialog(null, alunos.toString());
                    if ("1".equals(aluno.getBeneficiario())) {

                        enab.setEnabled(true);
                        jLabel5.setEnabled(true);
                        jRadioButton1.setEnabled(true);
                        jRadioButton2.setEnabled(true);
                        jRadioButton3.setEnabled(true);

                        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");

                        jTextField4.setText(format.format(new Date()));

                    } else {
                        enab.setEnabled(false);
                        jTxtNoMatricula.setText("");
                        jTxtNoMatricula.requestFocus();
                        JOptionPane.showMessageDialog(null, "Aluno não beneficiário ou número de matrícula errado");
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(ValidarTicket.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
    }//GEN-LAST:event_jLabel4MouseClicked

