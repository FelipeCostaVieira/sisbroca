    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked

        Refeicao ref = new Refeicao();
        RefeicaoDAO refDAO = new RefeicaoDAO();

        if (enab.getEnabled() == true) {
            try {
                ref.setIdAluno(jTxtNoMatricula.getText());
                SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
                Calendar c = Calendar.getInstance();

                c.setTime(formatoData.parse(jTextField4.getText()));
                ref.setDataRefeicao(c);

                if (jRadioButton1.isSelected()) {
                    ref.setCafe(1);
                    ref.setAlmoco(0);
                    ref.setJantar(0);

                } else {
                    if (jRadioButton2.isSelected()) {
                        ref.setCafe(0);
                        ref.setAlmoco(1);
                        ref.setJantar(0);

                    } else {
                        if (jRadioButton3.isSelected()) {
                            ref.setAlmoco(0);
                            ref.setCafe(0);
                            ref.setJantar(1);

                        }
                    }
                }
                int x = ref.getAlmoco();
                x += ref.getCafe();
                x += ref.getJantar();

                if (x == 0) {
                    JOptionPane.showMessageDialog(null, "Refeição não selecionada.");
                } else {

                    try {
                        refDAO.getPesquisaRefData(ref);
                    } catch (ParseException ex) {
                        Logger.getLogger(ValidarTicket.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    dispose();
                    new ValidarTicket();
                    dispose();

                }

            } catch (SQLException ex) {
                Logger.getLogger(ValidarTicket.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParseException ex) {
                Logger.getLogger(ValidarTicket.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {

        }

    }//GEN-LAST:event_jLabel5MouseClicked

